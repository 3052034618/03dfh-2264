def generate_real_transcripts():
    os.makedirs(TRANSCRIPTS_DIR, exist_ok=True)

    wav_files = [f for f in os.listdir(SAMPLES_DIR) if f.endswith('.wav')]

    count = 0
    for i, wav_file in enumerate(wav_files):
        if random.random() < 0.6:
            txt_name = os.path.splitext(wav_file)[0] + ".txt"
            txt_path = os.path.join(TRANSCRIPTS_DIR, txt_name)

            transcript = random.choice(real_transcripts)
            with open(txt_path, "w", encoding="utf-8") as f:
                f.write(transcript)

            count += 1

    print(f"✓ 已生成 {count} 份真实转写稿")
    print(f"  保存在: {TRANSCRIPTS_DIR}")
    print(f"  总录音数: {len(wav_files)}")
    print(f"  有转写稿比例: {count/len(wav_files)*100:.0f}%")


if __name__ == "__main__":
    random.seed(42)
    generate_real_transcripts()
